import streamlit as st

def display_backtest_help():
    """显示回测帮助信息"""
    with st.expander("回测说明", expanded=False):
        st.markdown("""
        ### 回测指标说明
        
        1. **收益指标**
           - 总收益率：策略整体收益表现
           - 年化收益率：将总收益率转化为年度收益率
           - 超额收益：相对于基准的超额收益部分
        
        2. **风险指标**
           - 最大回撤：策略最大的回撤幅度
           - 夏普比率：年化超额收益与波动率的比值
           - 信息比率：超额收益与跟踪误差的比值
        
        3. **交易统计**
           - 交易次数：策略总共进行的交易次数
           - 胜率：盈利交易占总交易的比例
           - 平均盈利/亏损：每笔交易的平均盈亏
        
        ### 策略说明
        
        1. **资金管理**
           - 单个持仓的资金比例限制
           - 最大持仓数量限制
           - 考虑交易成本（手续费）
        
        2. **选股逻辑**
           - 基于技术指标的动态选股
           - 股票池轮动机制
           - 风险控制措施
        
        3. **注意事项**
           - 回测结果仅供参考，实盘交易可能存在滑点
           - 建议结合基本面分析
           - 注意风险控制和仓位管理
        """)